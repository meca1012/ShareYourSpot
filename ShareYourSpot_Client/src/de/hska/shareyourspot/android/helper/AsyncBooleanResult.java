package de.hska.shareyourspot.android.helper;

public class AsyncBooleanResult {
	
	private boolean result;
	
	public AsyncBooleanResult(boolean booleanResult)
	{
		this.result = booleanResult;
		
	}

	public boolean isResult() {
		return result;
	}

	public void setResult(boolean result) {
		this.result = result;
	}
	

}